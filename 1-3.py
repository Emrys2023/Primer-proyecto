num1 = int (input("ingrese un numero: "))
num2 = int (input("ingrese otro numero: "))

if num1 > num2:
    suma = num1 + num2
    resta = num1 - num2
    print ("Suma: ", suma)
    print ("Resta: ", resta)
else:
    producto = num1 * num2
    division = num2 / num1
    print("Producto: ", producto)
    print ("Division: ", division)